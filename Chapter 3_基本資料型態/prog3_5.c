/* prog3_5, 數字字元與其相對應的ASCII 碼 */
# include <stdio.h>
# include <stdlib.h>
int main(void)
{

	char ch='2';                                 /* 宣告字元變數ch，並設值為’2’ */
	printf("ch= %c\n", ch);                 /* 列印出字元變數 ch */
	printf("ASCII of ch= %d\n", ch);   /* 列印出ch的ASCII碼 */


	system("pause");
	return 0;

}